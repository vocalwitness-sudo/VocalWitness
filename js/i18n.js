/**
 * Updates UI elements based on selected language
 */
export function translateUIElements(langCode) {
    const lexicon = translationDictionary[langCode] || translationDictionary.en;
    localStorage.setItem('preferredLang', langCode);

    // Main composer
    const mainInput = document.getElementById('mainInput');
    const postButton = document.getElementById('postButton');
    if (mainInput) mainInput.placeholder = lexicon.placeholder || "Provide raw description...";
    if (postButton) postButton.textContent = lexicon.postButton || "Publish to Decentralized Ledger";

    // Navigation Buttons
    const navButtons = document.querySelectorAll('.nav-btn');
    if (navButtons[0]) navButtons[0].textContent = lexicon.witnessVoice || "Witness Voice";
    if (navButtons[1]) navButtons[1].textContent = lexicon.citizenTalk || "Citizen Talk";
    if (navButtons[2]) navButtons[2].textContent = lexicon.liveArena || "Live Arena";

    // Profile button
    const profileBtn = document.getElementById('btn-profile');
    if (profileBtn) profileBtn.textContent = lexicon.profile || "Profile";

    // Active language display
    const activeBadge = document.getElementById('activeLanguageDisplay');
    if (activeBadge) activeBadge.textContent = lexicon.activeLanguage || "English (EN)";
}

/**
 * Auto-load user's last saved language
 */
export function initLanguage() {
    const savedLang = localStorage.getItem('preferredLang') || 'en';
    const langSelect = document.getElementById('languageSelector');
    if (langSelect) {
        langSelect.value = savedLang;
    }
    translateUIElements(savedLang);
}

/**
 * Change language
 */
export function changeLanguage(langCode) {
    translateUIElements(langCode);
}
